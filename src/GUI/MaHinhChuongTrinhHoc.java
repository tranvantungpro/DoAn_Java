     private void TaiDsChuongTrinhHoc()
    {
        ChuongTrinhHocDAL chuongtrinhhocdal = new ChuongTrinhHocDAL();
        String header[] = {"Mã Chương Trình Học", "Tên Chương Trình Học", "Học Phí"};
        tblModel.setColumnIdentifiers(header);
        tblModel.setRowCount(0);
        ArrayList<ChuongTrinhHocDTO> li = new ArrayList<ChuongTrinhHocDTO>();
        li = chuongtrinhhocdal.LayDanhSachChuongTrinhHoc();
        for(ChuongTrinhHocDTO cth : li)
        {
            Vector data =new Vector();
            
            data.add(cth.getMachuongtrinhhoc());
            data.add(cth.getTenchuongtrinhoc());
            data.add(cth.getHocphi());

            tblModel.addRow(data);
        }
        jtbChuongTrinhHoc.setModel(tblModel);
           
    }

