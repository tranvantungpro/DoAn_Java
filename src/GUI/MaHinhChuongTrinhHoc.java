    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int ret = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa mục này ?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (ret != JOptionPane.YES_OPTION) {
            return;
        }
        ChuongTrinhHocDAL cthdal = new ChuongTrinhHocDAL();
        jtbChuongTrinhHoc.getSelectedColumn();
        
        ret=cthdal.XoaTheoMa(dieukien);
        if(ret!=-1)
        {
            JOptionPane.showMessageDialog(this, "Xóa thành công");
            txtTenChuongTrinhHoc.setText("");
            txtHocPhi.setText("");
            TaiDsChuongTrinhHoc();
            
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Xóa không thành công");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

